// ── User Achievement helper ──────────────────────────────────────────────────

function ua(
  userId: string,
  achievementId: string,
  progress: number,
  isUnlocked: boolean,
  daysAgo?: number,
  rewardClaimed?: boolean
): UserAchievement {
  return {
    achievementId,
    userId,
    progress,
    isUnlocked,
    unlockedAt: isUnlocked
      ? new Date(Date.now() - (daysAgo ?? 0) * 86400000).toISOString()
      : undefined,
    rewardClaimed: isUnlocked ? (rewardClaimed ?? true) : false,
  };
}

